#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		ch;

	ch = 48;
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n == -2147483648)
		write(fd, "2147483648", 10);
	else if (n <= 9)
	{
		ch += n;
		write(fd, &ch, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
